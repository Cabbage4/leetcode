func longestSubarray(nums []int) int {
	ln := len(nums)
	var r int
	max := 0
	for i := 0; i < ln; {
		j := i + 1
		for j < ln && nums[i] == nums[j] {
			j++
		}

		if nums[i] > max || nums[i] == max && j-i > r {
			max = nums[i]
			r = j - i
		}
		i = j
	}
	return r
}

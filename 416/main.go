func canPartition(nums []int) bool {
	sum := 0
	for _, v := range nums {
		sum += v
	}

	if sum%2 != 0 {
		return false
	}

	target := sum / 2
	dp := make([][]bool, len(nums))
	for i := range dp {
		dp[i] = make([]bool, target+1)
		dp[i][0] = true
	}

	for i := 1; i <= target; i++ {
		if nums[0] == i {
			dp[0][i] = true
		}
	}

	for i := 1; i < len(nums); i++ {
		for j := 1; j <= target; j++ {
			if j < nums[i] {
				dp[i][j] = dp[i-1][j]
				continue
			}

			dp[i][j] = dp[i-1][j] || dp[i-1][j-nums[i]]
		}
	}

	return dp[len(nums)-1][target]
}
